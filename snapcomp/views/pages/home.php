  <h2>About SnapComp</h2>
  <p><font face="Satisfy" size="4">"Pick a pic"</font></p>


  <hr>
  <p>
    SnapComp is a real-time photography competition. It consists of two parts, the first one being the users' profile containg basic information.
    The second part is the game itself. The user must first register, providing some necessary information such as the username, account name and alike.
    The user can also browse winning pictures, without an acccount.
  <p>

    <br>
    <h4>The Game</h4>
  <p>
    The game consists of room with sessions. The users can enter the room, where they compete with other players by posting photographs relevant
    to the current theme. The room has two types of players, a selector and competitors. The selectors have the ability to select the theme for the next
    round at the start of a session and choose the best picture at the end of a session. The competitors are the ones, who take the pictures.
    The user whose picture was selected then recieves a point and a new selector is chosen. The winning pictures are automatically posted onto the website.
  </p>
